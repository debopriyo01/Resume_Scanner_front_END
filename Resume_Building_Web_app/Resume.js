//--> filter The Serach Value:-
function filtering(Search_Value) {

    if (Search_Value == 'software engineer') {

        const SoftWare_Developer = Data.resume.filter((items => items.basics.AppliedFor.toLowerCase() == Search_Value));
        DisplayData(SoftWare_Developer, index);

        No_Result.style.display = 'none';
        Resume.style.display = 'block';

    } else if (Search_Value == 'manager') {

        const Maneger = Data.resume.filter((items => items.basics.AppliedFor.toLowerCase() == Search_Value));
        DisplayData(Maneger, index);

        No_Result.style.display = 'none';
        Resume.style.display = 'block';

    } else if (Search_Value == 'hr') {

        const HR = Data.resume.filter((items => items.basics.AppliedFor.toLowerCase() == Search_Value));
        DisplayData(HR, index);

        No_Result.style.display = 'none';
        Resume.style.display = 'block';
    } else {

        No_Result.style.display = 'block';
        Resume.style.display = 'none';

    }

}
//--> for Display The data in The web Page :-
function DisplayData(Employee, i) {

    //--> Optinal For Calculeting The index of i :-
    console.log(i);

    //--> Base Condition :-
    if (i > Employee.length || i < -1) {
        return;
    }

    if (i > Employee.length - 1) {

        i = 0;
    }

    if (i < 0) {

        i = Employee.length - 1;

    }

    //--> if The only 1 Candidate for The post then Prev and Next will be invisible
    if (Employee.length == 1) {
        Next.style.visibility = 'hidden';
        Prev.style.visibility = 'hidden';
    } else {
        Next.style.visibility = 'visible';
        Prev.style.visibility = 'visible';
    }

    //--> Besic Details 
    Name.innerHTML = Employee[i].basics.name
    Post.innerHTML = Employee[i].basics.AppliedFor;
    num.innerHTML = Employee[i].basics.phone;
    Email.innerHTML = Employee[i].basics.email;

    //--> Adress :-
    Address.innerHTML = Employee[i].basics.location.address;
    Postal_Coad.innerHTML = Employee[i].basics.location.postalCode;
    city.innerHTML = Employee[i].basics.location.city;
    state.innerHTML = Employee[i].basics.location.state;

    //--> Profiles:-
    Social_Profile.innerHTML = Employee[i].basics.profiles.network;
    URl.innerHTML = Employee[i].basics.profiles.url;

    //--> Skills:-
    Skill_Name.innerHTML = Employee[i].skills.name;
    Skill_Level.innerHTML = Employee[i].skills.level;
    Skills.innerHTML = Employee[i].skills.keywords;

    //--> Work Experience :-
    Complay_Name.innerHTML = Employee[i].work.Company_Name;
    Position.innerHTML = Employee[i].work.Position;
    Start_Date.innerHTML = Employee[i].work.Start_Date;
    End_Date.innerHTML = Employee[i].work.End_Date;
    Summury.innerHTML = Employee[i].work.Summary;

    //--> Projects:-
    Name_of_Project.innerHTML = Employee[i].projects.name;
    Description.innerHTML = Employee[i].projects.description;

    //--> Educetion :-
    var Temp = Employee[i].education;
    UG.innerHTML = `${Temp.UG.institute},${Temp.UG.course},${Temp.UG.Start_Date},${Temp.UG.End_Date},${Temp.UG.cgpa}`;
    PG.innerHTML = `${Temp.Senior_Secondary.institute},${Temp.Senior_Secondary.cgpa}`;
    high_school.innerHTML = `${Temp.High_School.institute},${Temp.High_School.cgpa}`;

    //--> Intership:-
    var Temp2 = Employee[i].Internship;
    Complay_Name_Intern.innerHTML = Temp2.Company_Name;
    Position_Intern.innerHTML = Temp2.Position;
    Start_Date_Intern.innerHTML = Temp2.Start_Date;
    End_Date_Intern.innerHTML = Temp2.End_Date;
    Summury_Intern.innerHTML = Temp2.Summary;

    //--> Achivments :-
    var Temp3 = Employee[i].achievements.Summary;
    Achivments.innerHTML = Temp3;

    //--> Interest:-
    var Temp4 = Employee[i].interests.hobbies;
    Interest.innerHTML = Temp4;


    //--> Adding Listners To Next buttom :-
    Next.addEventListener('click', () => {


        if (i < Employee.length - 1) {

            return DisplayData(Employee, ++i);
        }

    });

    //-> Adding Listner To Prev Buttom :-
    Prev.addEventListener('click', () => {

        if (i > 0) {
            return DisplayData(Employee, --i);
        }
    });
};

